    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
                                
    // Mostrar cuadro de confirmación Sí / No
    int opcion = JOptionPane.showConfirmDialog(
        this,
        "¿Ya ha cargado empleados antes?",
        "Confirmación",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.QUESTION_MESSAGE
    );

    switch (opcion) {
        case JOptionPane.YES_OPTION -> {
            System.out.println("Usuario eligió SÍ");

            // --- Mostrar cuadros de texto para ingresar las rutas ---
            String rutaMarcajes = JOptionPane.showInputDialog(
                this,
                "Ingrese la ruta donde están los marcajes de empleados:",
                "Ruta de marcajes",
                JOptionPane.PLAIN_MESSAGE
            );

            String rutaCartas = JOptionPane.showInputDialog(
                this,
                "Ingrese la ruta donde desea guardar las cartas generadas:",
                "Ruta de cartas",
                JOptionPane.PLAIN_MESSAGE
            );

            // Verificar que ambas rutas no sean nulas o vacías
            if (rutaMarcajes != null && !rutaMarcajes.isEmpty() &&
                rutaCartas != null && !rutaCartas.isEmpty()) {

                // Crear el objeto Historial
                modelo.Historial historial = new modelo.Historial(rutaMarcajes, rutaCartas);

                // Enviar al controlador para guardar
                controlador.guardarHistorial(historial);

                JOptionPane.showMessageDialog(
                    this,
                    """
                    \u2705 Rutas guardadas correctamente:
                    
                    \ud83d\udcc2 Marcajes: """ + rutaMarcajes + "\n" +
                    "📁 Cartas: " + rutaCartas,
                    "Confirmación",
                    JOptionPane.INFORMATION_MESSAGE
                );

            } else {
                JOptionPane.showMessageDialog(
                    this,
                    "⚠️ Debe ingresar ambas rutas para continuar.",
                    "Error de entrada",
                    JOptionPane.WARNING_MESSAGE
                );
            }
        }

        case JOptionPane.NO_OPTION -> {
            System.out.println("Usuario eligió NO");
            this.cargarArchivo(0, "csv");
        }

        default -> System.out.println("Diálogo cerrado sin seleccionar Sí o No");
    }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

    // Solicitar usuario y contraseña
        String usuario = JOptionPane.showInputDialog(this, "Ingrese su nombre de usuario:", "Autenticación requerida", JOptionPane.PLAIN_MESSAGE);
        if (usuario == null || usuario.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Operación cancelada o usuario vacío.", "Cancelado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        JPasswordField campoPassword = new JPasswordField();
        int opcion = JOptionPane.showConfirmDialog(this, campoPassword, "Ingrese su contraseña:", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        if (opcion != JOptionPane.OK_OPTION) {
            JOptionPane.showMessageDialog(this, "Operación cancelada.", "Cancelado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String password = new String(campoPassword.getPassword());

        // 🔐 Validar credenciales
        // Aquí puedes cambiar los valores esperados o hacer una validación con tu sistema real
        String usuarioValido = "admin";
        String contrasenaValida = "1234";

        if (usuario.equals(usuarioValido) && password.equals(contrasenaValida)) {
            // Confirmar acción destructiva
            int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "⚠️ Esta acción eliminará TODOS los horarios de los empleados, para ingresar los nuevos horarios siga las instrucciones en ayuda",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
            );

            if (confirmacion == JOptionPane.YES_OPTION) {
                try {
                    // Llamar al controlador principal para eliminar los datos
                    controlador.getModelo().getHorarios().clear();
                    // Agrega aquí las listas o modelos que tengas

                    JOptionPane.showMessageDialog(this, "✅ Todos los datos fueron eliminados correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "❌ Error al eliminar datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Operación cancelada.", "Cancelado", JOptionPane.INFORMATION_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Credenciales incorrectas. No se realizaron cambios.", "Error de autenticación", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton5ActionPerformed

